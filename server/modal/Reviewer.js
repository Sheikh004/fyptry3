import mongoose from "mongoose";
const reviewerSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const reviewer = mongoose.model("reviewer", reviewerSchema);
export default reviewer;
