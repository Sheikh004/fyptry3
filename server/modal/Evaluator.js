import mongoose from "mongoose";
const evaluatorSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const evaluator = mongoose.model("evaluator", evaluatorSchema);
export default evaluator;
